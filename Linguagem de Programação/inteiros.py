# Crie um programa que exiba todos os números inteiros
# de 100 até 200 em ordem crescente. Depois crie outro programa que exiba de 200 até 100 em ordem decrescente. 

#crescente
x = 100
while x <= 200:
    print(x)
    x += 1

#decrescente
y = 200     
while y >= 100:
    print(y)
    y -= 1  
